package org.epmr.authenticate.dto;

import java.io.Serializable;

public class UserDTO implements Serializable
{
	/**
	 * 
	 */
	private static final long serialVersionUID = 8174093180119830372L;
	private String  loginCode;
	private String prefix;
    private String firstName;
    private String lastName;
    private String erpPersonId;
    private String email;
    private String officePhone;
    private String mobilePhone;
    private String qqNumber;
    private int userType;
    private String userTypeName;
    private boolean enabled;
    public UserDTO(){}
    public UserDTO( String  loginCode, String prefix, String firstName,
    		        String lastName, String erpPersonId, String email,
                    String officePhone, String mobilePhone, String qqNumber,
                    int userType,String userTypeName, boolean enabled)
    {
    	this.loginCode=loginCode;
    	this.prefix=prefix;
    	this.firstName=firstName;
    	this.lastName=lastName;
    	this.erpPersonId=erpPersonId;
    	this.email=email;
    	this.officePhone=officePhone;
    	this.mobilePhone=mobilePhone;
    	this.qqNumber=qqNumber;
    	this.userType=userType;
    	this.userTypeName=userTypeName;
    	this.enabled=enabled;
    }
    public String getLoginCode() {
		return loginCode;
	}
	public void setLoginCode(String loginCode) {
		this.loginCode = loginCode;
	}
	public String getPrefix() {
		return prefix;
	}
	public void setPrefix(String prefix) {
		this.prefix = prefix;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getErpPersonId() {
		return erpPersonId;
	}
	public void setErpPersonId(String erpPersonId) {
		this.erpPersonId = erpPersonId;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getOfficePhone() {
		return officePhone;
	}
	public void setOfficePhone(String officePhone) {
		this.officePhone = officePhone;
	}
	public String getMobilePhone() {
		return mobilePhone;
	}
	public void setMobilePhone(String mobilePhone) {
		this.mobilePhone = mobilePhone;
	}
	public String getQqNumber() {
		return qqNumber;
	}
	public void setQqNumber(String qqNumber) {
		this.qqNumber = qqNumber;
	}
	public int getUserType() {
		return userType;
	}
	public void setUserType(int userType) {
		this.userType = userType;
	}
	public String getUserTypeName() {
		return userTypeName;
	}
	public void setUserTypeName(String userTypeName) {
		this.userTypeName = userTypeName;
	}
	public boolean isEnabled() {
		return enabled;
	}
	public void setEnabled(boolean enabled) {
		this.enabled = enabled;
	}
	
	
}
